package p1;

public class sumofarray {

	
		    public static void main(String[] args) {
		        
		    	
		    	int number[] = {12,67,89,43};
		    	
		    	
		    	int sum=0;
		    	
		    	for(int i=0;i<number.length;i++)
		    	{
		    		
		    		
		    		sum+=number[i];
		    		
		    	}
		    	
		    	System.out.println("sum of array:"+sum);
		    	
		    }
		    
		    


		
	}


